// Tipos de Datos en Javascript
let string = (typeof 'Leonel'); // string
let number = (typeof 5); // number
let boolean = (typeof true); // boolean
let objectType = (typeof null); // object type
let undefinedType = (typeof undefined); // undefined

console.log(string, number, boolean, objectType, undefinedType);

// Declarando diferentes variables con distintos tipos de datos
let firstName = 'Leonel'; // first name of a person
let lastName = 'Marsilli'; // last name of a person
let country = 'Argentina'; // country
let city = 'Quilmes'; // capital city
let age = 25; // age in years
let isMarried = false;

console.log(firstName, lastName, country, city, age, isMarried);

// Operaciones matematicas basicas
let addition = (2 + 3); // Addition
let subtraction = (3 - 2); // Subtraction
let multiplication = (2 * 3); // Multiplication
let division = (3 / 2); // Division
let modulus = (3 % 2); // Modulus - finding remainder
let exponentiation = (3 ** 2); // Exponentiation 3 ** 2 == 3 * 3

console.log(addition, subtraction, multiplication, division, modulus, exponentiation);

